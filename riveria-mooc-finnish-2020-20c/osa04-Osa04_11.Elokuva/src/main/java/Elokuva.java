
public class Elokuva {
    private String nimi;
    private int ikaraja;
    
    public Elokuva(String elokuvanNimi, int elokuvanIkaraja){
        this.nimi=elokuvanNimi;
        this.ikaraja=elokuvanIkaraja;
    }
    
    public String nimi(){
        if(nimi==nimi){
            return nimi;
        }else{
            return null;
        }
    }
    
    public int ikaraja(){
        if(ikaraja==ikaraja){
            return ikaraja; 
        }else{
            return 0;
        }
    }


}
